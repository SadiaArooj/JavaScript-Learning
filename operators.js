
// Operators in js
//Arithmetic Operators
var a = 10;
var b = 5;
console.log("The addition of", a, "and", b, "is", a + b);

//Assignment Operators +, -, /, *

var c = b;
c += b; // c = c + b;
console.log(c);

// Comparison Operators ==, <=, >=, !=, <, >
var x = 10;
var y = 5;
console.log(x == y);
console.log(x <= y);

// Logical Operators &&, ||
console.log(true && true); // AND
console.log(true || false); // OR

console.log(!true); // NOT